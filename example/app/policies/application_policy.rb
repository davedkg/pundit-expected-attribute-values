# Base policy. Including the gem's Policy module adds the
# expected_attribute_values_for_action DSL and the matcher/assertion helpers.
class ApplicationPolicy
  include Pundit::ExpectedAttributeValues::Policy

  attr_reader :user, :record

  def initialize(user, record)
    @user = user
    @record = record
  end

  def index?
    false
  end

  def show?
    false
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

  class Scope
    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      scope.all
    end

    private

    attr_reader :user, :scope
  end
end
